#include "ft_printf.h"

size_t	ft_strlen(const char *str)
{
	size_t	len;

	len = 0;
	while (str[len] != '\0')
		len++;
	return (len);
}

int	ft_putstr(char *s, int fd)
{
	if (!s)
		return (write(fd, "(null)", 6));
	return (write (fd, s, ft_strlen(s)));
}
